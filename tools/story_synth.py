def _quote(base: str, i: int) -> str:
    tag = _pick([
        "现场如此", "账本如此", "对照如此", "余波如此", "盘面如此",
        "窗口如此", "激励如此", "叙事如此", "出口如此", "体感如此",
        "排期如此", "口径如此", "镜头如此", "选票如此", "算法如此",
        "合同如此", "菜单如此", "房租如此", "热搜如此", "补丁如此",
        "闸门如此", "合影如此", "声明如此", "演练如此", "订阅如此",
    ], i)
    b = (base or "对照可见。").rstrip("。")
    return f"{b}——{tag}。"


def _sum3(a: str, b: str, c: str, d: str = "", e: str = "") -> str:
    parts = [a, b, c]
    if d:
        parts.append(d)
    if e:
        parts.append(e)
    return "".join(parts)


